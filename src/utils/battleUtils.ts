export const convertExpToLevel = (_exp: number): number => {
  const exp = _exp || 0;
  let myLevel = 1;
  let expNeed = 0;
  for (let index = 0; index < LEVEL_EXP_SETTINGS.length; index++) {
    const level = index + 2;
    expNeed += LEVEL_EXP_SETTINGS[index];
    if (exp >= expNeed) {
      myLevel = level;
    } else {
      break;
    }
  }
  return myLevel;
};
